const nb = { style: BorderStyle.NONE, size: 0, color: "FFFFFF" };
const noBord = { top:nb, bottom:nb, left:nb, right:nb, insideH:nb, insideV:nb };
const thin = (c="CCCCCC") => ({ style: BorderStyle.SINGLE, size:1, color:c });
const cellB = (c="CCCCCC") => { const b=thin(c); return {top:b,bottom:b,left:b,right:b}; };

function TC(content, { w, bg=null, color=C.black, bold=false, sz=19, al=AlignmentType.LEFT,
  borders=null, span=1, italic=false, font="Calibri" }={}) {
  const items = Array.isArray(content) ? content : [content];
  const paras = items.map(c => c instanceof Paragraph ? c :
    new Paragraph({ alignment:al, spacing:{before:40,after:40},
      children:[new TextRun({text:String(c), bold, size:sz, color, font, italic})] }));
  const cfg = { children:paras, verticalAlign:VerticalAlign.CENTER,
    margins:{top:90, bottom:90, left:130, right:130} };
  if (bg) cfg.shading = { fill:bg, type:ShadingType.CLEAR };
  cfg.borders = borders || cellB();
  if (w) cfg.width = { size:w, type:WidthType.DXA };
  if (span>1) cfg.columnSpan = span;
  return new TableCell(cfg);
}

function hRow(texts, widths, bg=C.brand) {
  return new TableRow({ children: texts.map((t,i) =>
    TC(t, {w:widths[i], bg, color:C.white, bold:true, sz:18, al:AlignmentType.CENTER, borders:cellB(bg)})) });
}
function dRow(cells, widths, shade=false) {
  return new TableRow({ children: cells.map((c,i) =>
    TC(c, {w:widths[i], bg:shade?C.gray0:C.white, sz:18, color:C.gray5})) });
}
function mkT(heads, widths, rows, bg=C.brand) {
  const total = widths.reduce((a,b)=>a+b,0);
  return new Table({ width:{size:total,type:WidthType.DXA}, columnWidths:widths,
    rows:[hRow(heads,widths,bg), ...rows.map((r,i)=>dRow(r,widths,i%2===0))] });
}

